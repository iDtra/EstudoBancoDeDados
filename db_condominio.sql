    	ID SERIAL PRIMARY KEY,
    	NUMERO INT,
    	AREA DECIMAL(10,2),
   	 	ID_EDI INT,
   	 	FOREIGN KEY (ID_EDI) REFERENCES EDIFICIO(ID)
	);

