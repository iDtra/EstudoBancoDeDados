	CREATE TABLE VEICULO (
	    ID_VEI SERIAL PRIMARY KEY,
	    PLACA VARCHAR(10),
	    MODELO VARCHAR(50),
	    MARCA VARCHAR(50),
	    ID_CLI INT,
	    FOREIGN KEY (ID_CLI) REFERENCES CLIENTE(ID_CLI)
	);
	
