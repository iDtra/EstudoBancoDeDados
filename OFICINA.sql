	CREATE TABLE ITEM_ORCAMENTO (
	    ID_ITE SERIAL PRIMARY KEY,
	    ID_ORC INT,
	    ID_SER INT,
	    QUANTIDADE INT,
	    VALOR_TOT DECIMAL(10,2),
	    FOREIGN KEY (ID_ORC) REFERENCES ORCAMENTO(ID_ORC),
	    FOREIGN KEY (ID_SER) REFERENCES SERVICO(ID_SER)
	);
	
