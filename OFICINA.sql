	CREATE TABLE MECANICO (
	    ID_MEC SERIAL PRIMARY KEY,
	    NOME VARCHAR(100),
	    ESPECIALIDADE VARCHAR(100)
	);
	
	INSERT INTO MECANICO (NOME, ESPECIALIDADE)
	VALUES 
	('Carlos', 'Motor'),
	('Pedro', 'Freios');
	
	SELECT * FROM MECANICO

	CREATE TABLE ORCAMENTO (
	    ID_ORC SERIAL PRIMARY KEY,
	    DATA DATE,
	    STATUS VARCHAR(50),
	    VALOR_TOT DECIMAL(10,2),
	    ID_VEI INT,
	    ID_MEC INT,
	    FOREIGN KEY (ID_VEI) REFERENCES VEICULO(ID_VEI),
	    FOREIGN KEY (ID_MEC) REFERENCES MECANICO(ID_MEC)
	);
	
