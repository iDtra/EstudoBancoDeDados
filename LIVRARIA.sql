	CREATE TABLE ITEM_PEDIDO (
	    ID_ITE SERIAL PRIMARY KEY,
	    ID_PED INT,
	    ID_LIV INT,
	    QUANTIDADE INT,
	    VALOR_UNI DECIMAL(10,2),
	    FOREIGN KEY (ID_PED) REFERENCES PEDIDO(ID_PED),
	    FOREIGN KEY (ID_LIV) REFERENCES LIVRO(ID_LIV)
