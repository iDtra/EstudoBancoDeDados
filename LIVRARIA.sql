	CREATE TABLE PEDIDO (
	    ID_PED SERIAL PRIMARY KEY,
	    DATA DATE,
	    VALOR_TOT DECIMAL(10,2),
	    STATUS VARCHAR(50),
	    ID_CLI INT,
	    FOREIGN KEY (ID_CLI) REFERENCES CLIENTE(ID_CLI)
	);

