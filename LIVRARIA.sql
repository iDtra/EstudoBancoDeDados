CREATE DATABASE livraria
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'Portuguese_Brazil.1252'
    LC_CTYPE = 'Portuguese_Brazil.1252'
    LOCALE_PROVIDER = 'libc'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1
    IS_TEMPLATE = False;

	
	CREATE TABLE CLIENTE (
	    ID_CLI SERIAL PRIMARY KEY,
	    NOME VARCHAR(100),
	    TELEFONE VARCHAR(20),
	    ENDERECO VARCHAR(150)
	);

